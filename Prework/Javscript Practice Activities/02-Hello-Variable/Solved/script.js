// Declare variables using let
let userName = 'Sasuke';
let petCount = 1;
let interestingFact = 'I like pineapple on my pizza.';

// Use template literals to combine data and variable names in a single console log
console.log(`My name is ${userName}.`);
console.log(`I have ${petCount} pet(s).`);
console.log(`Fun fact: ${interestingFact}`);

// When re-assigning variables, use variable name
userName = 'Mateo';
petCount = 5;
interestingFact = 'I was a yo-yo champ in third grade.';

// Logs message with re-assigned values
console.log(`My name is ${userName}.`);
console.log(`I have ${petCount} pet(s).`);
console.log(`Fun fact: ${interestingFact}`);
